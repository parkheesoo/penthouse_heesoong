package com.penthouse_bogmjary;



public class Bookmark {

    private String buildingAddress;

    private String moneyMethod;

    private String bo;

    private String monthMoney;

    private String parking;

    private String animal;

    private String ev;

    private String manageMoney;

    private String big;

    private String something;

    private String monthlyFee;

    private String otherScore;

    public Bookmark(){}
    public Bookmark(String buildingAddress, String moneyMethod, String bo, String monthMoney, String parking, String animal, String ev, String manageMoney, String big, String something, String monthlyFee, String otherScore) {
        this.buildingAddress = buildingAddress;
        this.moneyMethod = moneyMethod;
        this.bo = bo;
        this.monthMoney = monthMoney;
        this.parking = parking;
        this.animal = animal;
        this.ev = ev;
        this.manageMoney = manageMoney;
        this.big = big;
        this.something = something;
        this.monthlyFee = monthlyFee;
        this.otherScore = otherScore;
    }


    public String getBuildingAddress() {
        return buildingAddress;
    }
    public void setBuildingAddress(String buildingAddress) {
        this.buildingAddress = buildingAddress;
    }

    public String getMoneyMethod() {
        return moneyMethod;
    }
    public void setMoneyMethod(String moneyMethod) {
        this.moneyMethod = moneyMethod;
    }

    public String getBo() {
        return bo;
    }
    public void setBo(String bo) {
        this.bo = bo;
    }

    public String getMonthMoney() {
        return monthMoney;
    }
    public void setMonthMoney(String monthMoney) {
        this.monthMoney = monthMoney;
    }

    public String getParking() {
        return parking;
    }
    public void setParking(String parking) {
        this.parking = parking;
    }

    public String getAnimal() {
        return animal;
    }
    public void setAnimal(String animal) {
        this.animal = animal;
    }

    public String getEv() {
        return ev;
    }
    public void setEv(String ev) {
        this.ev = ev;
    }

    public String getManageMoney() {
        return manageMoney;
    }
    public void setManageMoney(String manageMoney) {
        this.manageMoney = manageMoney;
    }

    public String getBig() {
        return big;
    }
    public void setBig(String big) {
        this.big = big;
    }

    public String getSomething() {
        return something;
    }
    public void setSomething(String something) {
        this.something = something;
    }

    public String getMonthlyFee() {
        return monthlyFee;
    }
    public void setMonthlyFee(String monthlyFee) {
        this.monthlyFee = monthlyFee;
    }

    public String getOtherScore() {
        return otherScore;
    }
    public void setOtherScore(String otherScore) {
        this.otherScore = otherScore;
    }

}


